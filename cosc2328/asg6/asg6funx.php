# if user clicks submit button on first page, check text input for html
# also check that text input for img height and width is not blank, non-numeric,
# or outside of the range of 200 to 800px;
# 	if text input generates error msg, redisplay first page, showing error msg(s), 
#   and repopulate text box(es)
function checkFormData()
{
	$errormsg = "";
	
    # check $imgHeight and $imgWidth for HTML
	$imgHeight = htmlentities($_POST['imgHeight']);
	$imgWidth = htmlentities($_POST['imgWidth']);

    # check $imgHeight:  if blank, show error msg
	if($imgHeight == "")
	{
		$imgHeight = "Please enter a height\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a height</h3></div>\n";
	}

    # check $imgHeight:  if non-numeric, show error msg
	else if(!is_numeric($imgHeight))
	{
		$imgHeight = "Please enter a height, numerically\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a height, ".
			"numerically</h3></div>\n";
	}

    # check $imgHeight:  if outside the range of 200 to 800 (pixels), show error msg
	else if($imgHeight < 200 || $imgHeight > 800)
	{
		$imgHeight = "Please enter a height between 200 and 800\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a height between 200 ".
			"and 800</h3></div>\n";
	}

    # check $imgWidth:  if blank, show error msg
	if($imgWidth == "")
	{
		$imgWidth = "Please enter a width\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a width</h3></div>\n";
	}

    # check $imgWidth:  if non-numeric, show error msg
	else if( !is_numeric($imgWidth))
	{
		$imgWidth = "Please enter a width, numerically\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a width, ".
			"numerically</h3></div>\n";
	}

    # check $imgWidth:  if outside the range of 200 to 800 (pixels), show error msg

	else if($imgWidth < 200 || $imgWidth > 800)
	{
		$imgWidth = "Please enter a width between 200 and 800\n";
		$errormsg .= "<div class='errortext'><h3>Please enter a width between 200 ".
			"and 800</h3></div>\n";
	}

    # if an error msg has been generated by the above checks, print the error msg, and 
    # resend data to showForm($imgHeight, $imgWidth); otherwise, draw img
	if($errormsg != "")
	{
		echo $errormsg;
		showForm($imgHeight, $imgWidth);
	}
	else
	{
	    echo "	<div>\n";
		drawImage($imgHeight, $imgWidth);
		echo "	<table><tr><td>";			
		showSubmitShowForm();
		echo "</td><td>";
		showSubmitMakeThumbnail();
		echo "</td></tr></table>\n	</div>\n";
	}
}

