# creates thumbnail image, prints original image, with thumbnail image beneath it 
# on the page
function makeThumbnail()
{
	$originalImage = imagecreatefrompng("../temp/tempPNG.png");
	
    # get dimensions of original image
	$origWidth = imagesx($originalImage);
	$origHeight = imagesy($originalImage);

    # calculate dimensions of thumbnail
	$newWidth = 80;
	$ratioOldToNewWidth = 80 / $origWidth;
	$newHeight = $origHeight*$ratioOldToNewWidth;

	echo "
		<table>
			<tr>
				<td>
					<div class='headingcontent'>
						<h2>Original image:<br /></h2>
					</div>
				</td>
			</tr>
			<tr>
				<td>
					<img src='../temp/tempPNG.png' alt='Original image' ".
						"height='$origHeight' width='$origWidth' />
				</td>
			</tr>
			<tr>
				<td>
					<div class='headingcontent'>
						<h2>Thumbnail image:<br /></h2>
					</div>
				</td>
			</tr>
			<tr align='center'>
				<td>
					<img src='../temp/tempPNG.png' alt='Thumbnail image' ".
						"height='$newHeight' width='$newWidth' />
				</td>
			</tr>
		</table>\n";

    # display a button to allow user to go back to image parameter selection page
	showSubmitShowForm();
}

